export function getNatalSign(day, month) {

    const signs = [
        ["Козерог", 19],
        ["Водолей", 18],
        ["Рыбы", 20],
        ["Овен", 19],
        ["Телец", 20],
        ["Близнецы", 20],
        ["Рак", 22],
        ["Лев", 22],
        ["Дева", 22],
        ["Весы", 22],
        ["Скорпион", 21],
        ["Стрелец", 21],
        ["Козерог", 31]
    ]

    return day > signs[month - 1][1]
        ? signs[month][0]
        : signs[month - 1][0]

}